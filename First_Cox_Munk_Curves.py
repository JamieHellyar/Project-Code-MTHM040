import numpy as np
from scipy.interpolate import interp1d

# Now to create the desired range of zenith angles
zenith_angles = np.linspace(-20, 70, 400)

curves = {}         # Empty list for the curves to be filled later

# Now to put all curves in the same array
for i, data in dig.items():
    x = np.array(data["x"])             # Viewing angle
    y = np.array(data["y"])             # Reflectance
    
    # To create the function to interpolate each curve
    f = interp1d(
        x, y,
        bounds_error=False,
        fill_value=np.nan               # To avoid extrapolation, return no values
    )
    
    # To fill in the interpolated curve data points
    curves[i] = f(zenith_angles)



# Then I can plot the curves 

import matplotlib.pyplot as plt


plt.figure(figsize=(10, 6))

# I need to define a colourmap first that can cover all 16 wind speeds being plotted
cmap = plt.get_cmap('jet', 16)

for i in range(0, 16):
    plt.plot(zenith_angles, curves[i], 
             label=f"{i} m/s", 				# For the wind speed labels
             color=cmap(i),  				# To use the colour map
             linewidth=2)


# For the plot labels:
plt.xlabel("Viewing zenith angle (deg)", fontsize=18)
plt.ylabel("Reflectance", fontsize=18)
plt.xticks(fontsize=14)
plt.yticks(fontsize=14)

# For the plot legend
plt.legend(title='Wind Speeds',ncol=2, fontsize=14)

plt.grid(True, linestyle='--', alpha=0.6)			# For the gridlines

plt.tight_layout()
plt.show()






